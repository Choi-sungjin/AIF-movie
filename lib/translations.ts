const LANG_STORAGE_KEY = "future-cinema-lang";

export function getStoredLang(): Lang {
  if (typeof window === "undefined") return "Kr";
  const stored = localStorage.getItem(LANG_STORAGE_KEY);
  return stored && (stored === "Kr" || stored === "En" || stored === "Jp") ? stored : "Kr";
}

export function setStoredLang(lang: Lang): void {
  if (typeof window !== "undefined") localStorage.setItem(LANG_STORAGE_KEY, lang);
}

export function t(lang: Lang, key: string): string {
  const map = translations[lang];
  return map[key] ?? translations.Kr[key] ?? key;
}
